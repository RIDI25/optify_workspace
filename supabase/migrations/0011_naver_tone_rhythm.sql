-- ============================================================
-- 옵티파이 워크스페이스 — 네이버 어미 리듬 개선 (0011)
-- naver_blog 프리셋 tone_rules를 '~합니다/~해요' 혼합 리듬으로 교체.
-- tone_rules 배열만 갱신(멱등 — 여러 번 실행해도 동일). DML이라 라이브 적용도 가능.
-- ============================================================

update channel_settings
set preset = jsonb_set(
  preset,
  '{tone_rules}',
  $json$
  [
    "'~합니다' 체와 '~인데요/~해요' 체를 혼합해 리듬을 만든다. 정보 전달·설명·단정은 '~합니다/~입니다'로 짚고, 독자에게 말 걸기·부연·전환은 '~인데요/~해보세요/~있어요'로 푼다",
    "같은 어미가 3문장 연속되지 않게 한다",
    "전체적으로는 워드프레스보다 가볍고 친근하게 — 문어체 딱딱함은 피하되, 전부 구어로 풀지도 않는다",
    "특정 직군을 호명하지 않는 범용 독자 대상",
    "도입: 인사 없이 독자의 상황·고민 짚기 + 메인 키워드 포함 + 글에서 얻을 것 예고",
    "문단은 2~4문장, 줄바꿈 넉넉하게",
    "통계·출처는 필수 아님 — 확실한 것만 쓰고, 불확실하면 쓰지 않음",
    "실행 지시는 '~해보세요' 형태로 구체적으로"
  ]
  $json$::jsonb
)
where channel = 'naver_blog';
