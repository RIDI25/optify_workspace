/** 등급 산정 — 표지·요약에 표기 */
export function scoreGrade(score: number): { grade: string; label: string } {
  if (score >= 90) return { grade: "A", label: "우수" };
  if (score >= 75) return { grade: "B", label: "양호" };
  if (score >= 50) return { grade: "C", label: "개선 필요" };
  return { grade: "D", label: "취약" };
}
